Rails:
- Rails é um Framework Web escrito em Ruby, criado em 2004 por David Heinemeier Hansson (DHH).
- Rails era o código fonte da empresa Basecamp, do David.
- Rails é otimizado para a felicidade da pessoa programando
- O Framwork funciona por padrão, é Convenção sobre configuração. Não necessita diversas configurações para funcionar.
- Padrão MVC (Model View Controller), significa que as camadas do código ficam isoladas em modelo (códigos que irão se comunicar com o banco de dados). Views são as telas exibidas aos usuários. Controllers que são os códigos que receberão as sequencias do navegador e realizarão a conexão entre as etapas.

